sparse.LUPsolve = function LUPsolve(lup,b) {
    var L = lup.L, U = lup.U, P = lup.P;
    var n = L.length, i,j, ret = new Array(n), accum, Ai,foo;
    for(i = 0;i<n;i++) {
        if(b.hasOwnProperty(P[i])) accum = b[P[i]];
        else accum = 0;
        Ai = L[i];
        for(j in Ai) {
            if(!Ai.hasOwnProperty(j)) continue;
            if(j<i) { accum -= Ai[j]*ret[j]; }
        }
        ret[i] = accum;
    }
    for(i = n-1;i>=0;i--) {
        accum = ret[i];
        Ai = U[i];
        for(j in Ai) {
            if(!Ai.hasOwnProperty(j)) continue;
            if(j>i) { accum -= Ai[j]*ret[j]; }
        }
        ret[i] = accum/Ai[i];
    }
    return ret;
}

sparse.scatter = function scatter(V) {
    var n = V[0].length, Vij, i, j, m = V.length, A = [], Aj;
    for(i=n-1;i>=0;--i) {
        if(!V[m-1][i]) continue;
        Aj = A;
        for(j=0;j<m-2;j++) {
            Vij = V[j][i];
            if(!Aj[Vij]) Aj[Vij] = [];
            Aj = Aj[Vij];
        }
        Aj[V[j][i]] = V[j+1][i];
    }
    return A;
}

sparse.gather = function gather(A,ret,k) {
    if(typeof ret === "undefined") ret = [];
    if(typeof k === "undefined") k = [];
    var n,i,Ai;
    n = k.length;
    for(i in A) {
        if(A.hasOwnProperty(i)) {
            k[n] = parseInt(i);
            Ai = A[i];
            if(typeof Ai === "number") {
                if(Ai) {
                    if(ret.length === 0) {
                        for(i=n+1;i>=0;--i) ret[i] = [];
                    }
                    for(i=n;i>=0;--i) ret[i].push(k[i]);
                    ret[n+1].push(Ai);
                }
            } else gather(Ai,ret,k);
        }
    }
    if(k.length>n) k.pop();
    return ret;
}

